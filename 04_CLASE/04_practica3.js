let palabra = "mesa";

switch(palabra.toLowerCase()){
    case "gato":
        console.log("cat");
         break;
    case "perro":
        console.log("dog");
         break;
    case "ventana":
        console.log("window");
         break;
    case "mesa":
        console.log("table");
         break;
    default:
        console.log("La palabra ingresada es incorrecta")     
}